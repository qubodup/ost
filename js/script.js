insert_items = function(source) {
    var node = $('div#content > p:first');
    for (var i = 0; i < Items[source].length; i++) {
        node.after(function() {
            /*console.log(i);/**/
            var it = Items[source][i];
            var img = Images[it.image];
            var el = '<div class="item">\n'
                + '  <a class="item" href="' + it.href + '">\n'
                + '  <img src="' + img.src + '" alt="' + img.alt + '" />\n'
                + '  <span>' + it.desc + '</span></a>\n'
                + '  <a class="source" href="' + img.href + '">image source</a>\n'
                + '</div>';
            return el;
        });
        node = $('div#content > div.item:last');
    }
};

$(document).ready(function() {
    $('select#src').change(function() {
        var source = $('select#src option:selected').get(0).value;
        $('div#content > div.item').remove();
        insert_items(source);
    });
    insert_items('oga');
});
